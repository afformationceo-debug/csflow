export function convertToCSV(templates: KnowledgeTemplate[]): string {
  const headers = ["카테고리", "질문", "답변"];
  const rows = templates.map((t) => [
    `"${t.category}"`,
    `"${t.question.replace(/"/g, '""')}"`,
    `"${t.answer.replace(/"/g, '""')}"`,
  ]);
  return [headers.join(","), ...rows.map((r) => r.join(","))].join("\n");
}
